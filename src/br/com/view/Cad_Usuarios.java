    public void preencherPerson() {
        con.conexao();
        con.executaSql("select * from tb_persons order by idperson desc limit 1;");
        try {
            con.rs.first();

            do {
                CodPer = Integer.parseInt(con.rs.getString("idperson"));

            } while (con.rs.next());

        } catch (SQLException ex) {
            //JOptionPane.showMessageDialog(null, "Erro na Consulta Persons/nErro!" + ex);
        }
    }

