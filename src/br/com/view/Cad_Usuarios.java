    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Persons persons = new Persons();
        persons.setNome(txtperson.getText());
        persons.setEmail(txtdesemail.getText());
        persons.setPhone(txtnrpPhone.getText());
        persondao.save(persons);
        preencherPerson();

        persons.setId_person(CodPer);

        Usuario usuario = new Usuario();
        usuario.setPerson(persons);
        usuario.setLogin(txtdeslogin.getText());
        usuario.setSenha(CriptografarSenha.encriptografar(txtdesSenha.getText()));
        usuario.setPerfil((String) comboinadimin.getSelectedItem());
        userdao.save(usuario);
        JOptionPane.showMessageDialog(null, "Usuário Salvo!");
        LimparCampos();
    }//GEN-LAST:event_btnSalvarActionPerformed

